using System;

namespace NetCore.Common
{
    /// <summary>
    /// Supports up to 14 different indexes.
    /// </summary>
    /// <remarks>
    /// 8 bits at the left are used by <see cref="QuickIndexPosition"/> for mapping.
    /// </remarks>
    [Flags]
    public enum QuickIndexMask : ulong
    {
        None     = 0b0_0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_0uL,
        One      = 0b0_0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_1uL,
        Two      = 0b0_0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_11_0uL,
        Three    = 0b0_0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_11_00_0uL,
        Four     = 0b0_0000000_0000000_000000_000000_00000_00000_0000_0000_000_111_00_00_0uL,
        Five     = 0b0_0000000_0000000_000000_000000_00000_00000_0000_0000_111_000_00_00_0uL,
        Six      = 0b0_0000000_0000000_000000_000000_00000_00000_0000_1111_000_000_00_00_0uL,
        Seven    = 0b0_0000000_0000000_000000_000000_00000_00000_1111_0000_000_000_00_00_0uL,
        Eight    = 0b0_0000000_0000000_000000_000000_00000_11111_0000_0000_000_000_00_00_0uL,
        Nine     = 0b0_0000000_0000000_000000_000000_11111_00000_0000_0000_000_000_00_00_0uL,
        Ten      = 0b0_0000000_0000000_000000_111111_00000_00000_0000_0000_000_000_00_00_0uL,
        Eleven   = 0b0_0000000_0000000_111111_000000_00000_00000_0000_0000_000_000_00_00_0uL,
        Twelve   = 0b0_0000000_1111111_000000_000000_00000_00000_0000_0000_000_000_00_00_0uL,
        Thirteen = 0b0_1111111_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_0uL,
    }
}