using System;

namespace NetCore.Common
{
    /// <summary>
    /// Supports up to 14 different indexes.
    /// </summary>
    /// <remarks>
    /// 8 bits at the left are used by <see cref="QuickIndexPosition"/> for mapping.
    /// </remarks>
    [Flags]
    public enum QuickIndexMask : ulong
    {
        None     = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_0_0uL, // of: 0
        One      = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_0_1uL, // of: 0
        Two      = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_1_0uL, // of: 1
        Three    = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_00_11_0_0uL, // of: 2
        Four     = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_000_000_11_00_0_0uL, // of: 4
        Five     = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_000_111_00_00_0_0uL, // of: 6
        Six      = 0b0000000_0000000_000000_000000_00000_00000_0000_0000_111_000_00_00_0_0uL, // of: 9
        Seven    = 0b0000000_0000000_000000_000000_00000_00000_0000_1111_000_000_00_00_0_0uL, // of: 12
        Eight    = 0b0000000_0000000_000000_000000_00000_00000_1111_0000_000_000_00_00_0_0uL, // of: 16
        Nine     = 0b0000000_0000000_000000_000000_00000_11111_0000_0000_000_000_00_00_0_0uL, // of: 20
        Ten      = 0b0000000_0000000_000000_000000_11111_00000_0000_0000_000_000_00_00_0_0uL, // of: 25
        Eleven   = 0b0000000_0000000_000000_111111_00000_00000_0000_0000_000_000_00_00_0_0uL, // of: 30
        Twelve   = 0b0000000_0000000_111111_000000_00000_00000_0000_0000_000_000_00_00_0_0uL, // of: 36
        Thirteen = 0b0000000_1111111_000000_000000_00000_00000_0000_0000_000_000_00_00_0_0uL, // of: 42
        Fourteen = 0b1111111_0000000_000000_000000_00000_00000_0000_0000_000_000_00_00_0_0uL, // of: 49
    }
}